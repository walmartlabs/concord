import java.util.Collection;
import java.util.Map;
import java.util.Set;

public class SanitizedMap<K, V> implements Map<K, V> {

    private final Map<K, V> delegate;

    public SanitizedMap(Map<K, V> delegate) {
        this.delegate = delegate;
    }

    @Override
    public int size() {
        return delegate.size();
    }

    @Override
    public boolean isEmpty() {
        return delegate.isEmpty();
    }

    @Override
    public boolean containsKey(Object key) {
        return delegate.containsKey(key);
    }

    @Override
    public boolean containsValue(Object value) {
        return delegate.containsValue(value);
    }

    @Override
    @SuppressWarnings({"unchecked", "rawtypes"})
    public V get(Object key) {
        V result = delegate.get(key);
        if (result instanceof Map) {
            return (V) new SanitizedMap((Map)result);
        }
        return result;
    }

    @SuppressWarnings("unchecked")
    @Override
    public V put(K key, V value) {
        V sanitized = (V) VariablesSanitizer.sanitize(value);
        return delegate.put(key, sanitized);
    }

    @Override
    public V remove(Object key) {
        return delegate.remove(key);
    }

    @Override
    public void putAll(Map<? extends K, ? extends V> m) {
        m.forEach(this::put);
    }

    @Override
    public void clear() {
        delegate.clear();
    }

    @Override
    public Set<K> keySet() {
        return delegate.keySet();
    }

    @Override
    public Collection<V> values() {
        return delegate.values();
    }

    @Override
    public Set<Entry<K, V>> entrySet() {
        return delegate.entrySet();
    }
}
