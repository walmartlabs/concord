import javax.ws.rs.WebApplicationException;
import javax.ws.rs.core.Response;
import javax.ws.rs.ext.Provider;

/**
 * Based on the original {@link org.sonatype.siesta.WebappExceptionMapper}.
 */
@Provider
public class WebappExceptionMapper extends ExceptionMapperSupport<WebApplicationException> {

    @Override
    protected Response convert(final WebApplicationException exception) {
        return exception.getResponse();
    }
}
