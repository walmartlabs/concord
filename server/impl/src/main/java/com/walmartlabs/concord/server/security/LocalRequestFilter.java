import com.walmartlabs.concord.server.sdk.security.AuthenticationException;

import javax.servlet.*;
import javax.servlet.http.HttpServletRequest;
import java.io.IOException;
import java.net.InetAddress;
import java.net.UnknownHostException;

public class LocalRequestFilter implements Filter {

    @Override
    public void init(FilterConfig filterConfig) {
        // do nothing
    }

    @Override
    public void doFilter(ServletRequest request, ServletResponse response, FilterChain chain) throws IOException, ServletException {
        HttpServletRequest req = (HttpServletRequest) request;
        if (isLocalRequest(req)) {
            chain.doFilter(request, response);
        } else {
            throw new AuthenticationException("Only localhost requests are allowed");
        }
    }

    @Override
    public void destroy() {
        // do nothing
    }


    private static boolean isLocalRequest(HttpServletRequest request) throws UnknownHostException {
        InetAddress addr = InetAddress.getByName(request.getRemoteAddr());
        return addr.isAnyLocalAddress() || addr.isLoopbackAddress();
    }
}
